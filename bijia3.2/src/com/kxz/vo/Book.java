package com.kxz.vo;

public class Book {
	private int bookid;
	private String bookname;
	private double bookprice;
	private String bookweb;
	private String bookauthor;
	private String bookpicture;
	private String bookisbn;
	private String bookpubtime;
	private String bookcbs;
	public int getBookid() {
		return bookid;
	}
	public void setBookid(int bookid) {
		this.bookid = bookid;
	}
	public String getBookname() {
		return bookname;
	}
	public void setBookname(String bookname) {
		this.bookname = bookname;
	}
	public double getBookprice() {
		return bookprice;
	}
	public void setBookprice(double bookprice) {
		this.bookprice = bookprice;
	}
	public String getBookweb() {
		return bookweb;
	}
	public void setBookweb(String bookweb) {
		this.bookweb = bookweb;
	}
	public String getBookauthor() {
		return bookauthor;
	}
	public void setBookauthor(String bookauthor) {
		this.bookauthor = bookauthor;
	}
	public String getBookpicture() {
		return bookpicture;
	}
	public void setBookpicture(String bookpicture) {
		this.bookpicture = bookpicture;
	}
	public String getBookisbn() {
		return bookisbn;
	}
	public void setBookisbn(String bookisbn) {
		this.bookisbn = bookisbn;
	}
	public String getBookpubtime() {
		return bookpubtime;
	}
	public void setBookpubtime(String bookputime) {
		this.bookpubtime = bookputime;
	}
	public String getBookcbs() {
		return bookcbs;
	}
	public void setBookcbs(String bookcbs) {
		this.bookcbs = bookcbs;
	}
	
	
}
